package jugador;

public class InfoPartida {
	private int mano;
	private int puntos;
	private int monedas;
	
	public int getMano() {
		return mano;
	}
	public void setMano(int mano) {
		this.mano = mano;
	}
	public int getPuntos() {
		return puntos;
	}
	public void setPuntos(int puntos) {
		this.puntos = puntos;
	}
	public int getMonedas() {
		return monedas;
	}
	public void setMonedas(int monedas) {
		this.monedas = monedas;
	}
}
